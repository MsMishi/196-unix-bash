#Exercise 1: What's the command to list all the non-hidden files and directories that start with the letter "s"?
$ ls s*

#Exercise 2: What is the command to list all the non-hidden files that contain the string 'onnet', long-form by reverse modification time?
$ ls *onnet* -rtl

#Exercise 3: What is the command to list all files (including hidden ones) by reverse modification time, in long form?
$ ls -a -rtl
